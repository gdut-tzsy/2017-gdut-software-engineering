public class SchoolClassVO {
    /**
     * The Department id.
     */
    private java.lang.String departmentId;
    /**
     * The Class full name.
     */
    private java.lang.String classFullName;
    /**
     * The Student num.
     */
    private java.lang.Integer studentNum;

    /**
     * Gets department id.
     *
     * @return the department id
     */
    public String getDepartmentId() {
        return departmentId;
    }

    /**
     * Sets department id.
     *
     * @param departmentId the department id
     */
    public void setDepartmentId(String departmentId) {
        this.departmentId = departmentId;
    }

    /**
     * Gets class full name.
     *
     * @return the class full name
     */
    public String getClassFullName() {
        return classFullName;
    }

    /**
     * Sets class full name.
     *
     * @param classFullName the class full name
     */
    public void setClassFullName(String classFullName) {
        this.classFullName = classFullName;
    }

    /**
     * Gets student num.
     *
     * @return the student num
     */
    public Integer getStudentNum() {
        return studentNum;
    }

    /**
     * Sets student num.
     *
     * @param studentNum the student num
     */
    public void setStudentNum(Integer studentNum) {
        this.studentNum = studentNum;
    }
}
